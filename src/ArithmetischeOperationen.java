public class ArithmetischeOperationen {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;

        int multi = a*b;
        int p = 2 + multi;
        System.out.println(p);

        System.out.println("Addition: " + (a+b));
        System.out.println(a-b);
        System.out.println(multi);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
